//1 - Criar um loop que conte de 1 até 10 usando FOR
console.log("Criar loop que conte de 1 até 10 usando FOR.")
for (let count = 1; count <= 10; count += 1){
    console.log(count)
} 

//2 - Criar um loop que conte de 10 até 1 usando WHILE
console.log("Criar loop que conte de 10 até 1 usando WHILE.")
let conte = 10

while (conte >= 1) {
    console.log(conte)
    conte -= 1
}

//3 - Criar um loop que conte todos os números ímpares de 1 até 100
console.log("Criar loop que conte os números ímpares de 1 até 100.")
let contador = 1

while(100 >=  contador){
    console.log(contador);
    contador +=2
}

